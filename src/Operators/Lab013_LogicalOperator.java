package Operators;
//1.
//    - `&&`  (AND)
//    - `||`  (OR)
//    - `!`  (NOT) - REVERSE
public class Lab013_LogicalOperator {
    public static void main(String[] args) {
        boolean a = false;
        boolean b = true;
        System.out.println(a&&b);
        System.out.println(a||b);
        System.out.println(!a);

    }
}




























































































































































































































































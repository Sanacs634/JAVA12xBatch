package Operators;
//1. **Relational Operators (Used to compare two values.)**
//    1. `<`  (Less than)
//    2. `<=`   (Less than or equal to)
//    3. `>`   (Greater than)
//    4. `>=`   (Greater than or equal to)
//    5. `==`   (Equal to)
//    6. `!=`   (Not equal to)
public class Lab012_RelationalOperator {
    public static void main(String[] args) {
        int a =11, b = 5;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a!=b);

    }
}
